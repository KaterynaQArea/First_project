package HW_lesson2;

public class ConvertCelsiusToKelvin {
    public   static   void   main(String[]   args)   {
        double   celsiusTemperature   =   50;
        double   kelvinTemperature   =   celsiusTemperature + 273.16;
        System. out .println("For  celsius  temperature   "   +   celsiusTemperature
                +   "  kelvin   temperature   is:   "   +   kelvinTemperature);
    }

}
//Formula:    Kelvin   =   C   +   273.16