package HW_lesson2;

public class Convert_Celsius_to_Fahrenheit {
    public   static   void   main(String[]   args)   {
        double   celsiusTemperature   =   50;
        double   fahrenheitTemperature   =   (celsiusTemperature*9/5d)+32;
        System. out .println("For  celsius  temperature   "   +   celsiusTemperature
                +   " fahrenheit   temperature   is:   "   +   fahrenheitTemperature);
    }
}
